<script>
  import logo from './assets/svelte.png'
  import Counter from './lib/Counter.svelte'

  let name = "";
  let greeting = "";

  function greet() {
    window.go.main.App.Greet(name).then((result) => {
      greeting = result;
    });
  }
</script>

<main>
  <img src={logo} alt="Svelte Logo" />
  <h1>
    Hello world!
  </h1>

  <Counter />

  <p>
    Visit <a href="https://svelte.dev">svelte.dev</a> to learn how to build Svelte
    apps.
  </p>

  <p>
    Check out <a href="https://github.com/sveltejs/kit#readme">SvelteKit</a> for
    the officially supported framework, also powered by Vite!
  </p>

  <hr>

  <p>This tests that the Go functionality is working too. The greeting is generated by Go.</p>
  <div id="input" data-wails-no-drag>
    <input id="name" type="text" bind:value={name} class="border-amber-600 border">
    <button class="button border-black border" on:click={greet}>Greetings</button>
  </div>
  {#if greeting}
    <div id="result">{greeting}</div>
  {/if}
</main>

